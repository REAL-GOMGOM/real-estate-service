/**
 * MLTM(국토부) 실거래 aptNm 정제 규칙 엔진 — "실거래 데이터 품질" Phase 1.
 *
 * 배경: 국토부 원천 데이터의 단지명이 동·괄호 표기로 쪼개져
 * 같은 단지가 여러 이름으로 흩어진다 (예: 쌍용스위닷홈(201동) / 쌍용스위닷홈(104동)).
 *
 * 결정 사항 (인계 문서):
 * - 괄호 표기 제거 — 단, 괄호 안이 차수/단지면 언랩해 본문에 부착
 * - 후행 동(棟) 표기 제거
 * - 차수(N차·N단지)는 유지 — 차수별 시세가 달라 병합하면 안 됨
 * - 오타 통합(스위닷홈↔스윗닷홈 등)은 규칙으로 불가능 → apt-aliases 확장으로 Phase 2에서
 *
 * 원칙: fail-open — 정제 결과가 빈 문자열이 되면 원본(트림만)을 반환한다.
 */
export function normalizeMLTMName(raw: string): string {
  if (typeof raw !== 'string') return '';
  const original = raw.trim();
  let s = original;

  // 1. 괄호 처리 (반각·전각) — 내용이 "N차"/"N단지"면 언랩 부착, 그 외는 통째 제거
  //    '래미안(1차)' → '래미안1차' / '쌍용스위닷홈(201동)' → '쌍용스위닷홈'
  s = s.replace(/\s*[(（]([^)）]*)[)）]/g, (_match, inner: string) => {
    const t = String(inner).trim();
    return /^\d+\s*(차|단지)$/.test(t) ? t.replace(/\s+/g, '') : '';
  });

  // 2. 후행 동(棟) 표기 제거 — 범위 → 단일 순서로 (지명 '~동' 오탐 방지 위해 숫자+동 한정)
  //    범위: '현대1차101동~106동' → '현대1차' / '삼호1동~3동' → '삼호'
  s = s.replace(/\s*\d{1,4}동\s*[~\-–]\s*\d{1,4}동$/, '');
  //    단일·열린 범위: '현대아파트101동' → '현대아파트' / '선경101동~' → '선경'
  s = s.replace(/\s*\d{1,4}동\s*[~\-–]?$/, '');

  // 3. 공백 정리
  s = s.replace(/\s+/g, ' ').trim();

  // fail-open: 정제가 이름을 전부 소거했으면 원본 유지
  return s.length > 0 ? s : original;
}
