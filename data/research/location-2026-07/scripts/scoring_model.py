# 3. z-score 기반 점수 산정 엔진
# ============================================================

WEIGHT_SCENARIOS = {
    'Base':   [0.25, 0.20, 0.20, 0.15, 0.10, 0.10],
    'Price':  [0.40, 0.20, 0.15, 0.10, 0.10, 0.05],
    'Growth': [0.15, 0.30, 0.25, 0.10, 0.10, 0.10],
    'Infra':  [0.20, 0.15, 0.30, 0.15, 0.10, 0.10],
}

INDICATORS = ['평당가', '매매누계_2025', '교통', '학군', '산업', '공급']


def compute_scores(df, weights=None, within_group=False):
    """
    z-score 가중합 → sigmoid → 1~5 매핑
    
    Parameters:
    -----------
    df : DataFrame
        원시 데이터
    weights : list[float], optional
        6개 지표 가중치. 기본값은 Base 시나리오.
    within_group : bool
        True면 그룹별(서울/경기/부산 등) 내에서 z-score 계산
        False면 전국 전체 z-score
    
    Returns:
    --------
    DataFrame with columns:
        재산정점수, 점수차, 각 지표 z-score
    """
    if weights is None:
        weights = WEIGHT_SCENARIOS['Base']
    assert abs(sum(weights) - 1.0) < 1e-6, "가중치 합이 1이 아닙니다"
    
    df = df.copy()
    
    # 3기신도시·기타(_NA)는 별도 처리 — 점수 그대로 유지
    valid_mask = df['평당가'].notna()
    
    # z-score 계산
    for col in INDICATORS:
        z_col = f'z_{col}'
        if within_group:
            df[z_col] = df.groupby('region')[col].transform(
                lambda x: (x - x.mean()) / (x.std() + 1e-9)
            )
        else:
            # 전국 z-score (유효 데이터만)
            series = df.loc[valid_mask, col]
            mu, sigma = series.mean(), series.std()
            df[z_col] = (df[col] - mu) / (sigma + 1e-9)
    
    # 가중합
    z_cols = [f'z_{c}' for c in INDICATORS]
    df['z_weighted'] = sum(w * df[zc] for w, zc in zip(weights, z_cols))
    
    # sigmoid → 0~1
    df['z_sigmoid'] = 1 / (1 + np.exp(-df['z_weighted']))
    
    # 1~5 매핑 (z_sigmoid가 클수록 → 상급 → 점수 낮음)
    # sigmoid=1.0(최상급) → 1.0, sigmoid=0.0(최하급) → 5.0
    df['재산정점수'] = 5.0 - 4.0 * df['z_sigmoid']
    df['재산정점수'] = df['재산정점수'].round(2)
    
    # _NA 3기신도시는 원본점수 유지
    df.loc[~valid_mask, '재산정점수'] = df.loc[~valid_mask, 'original_score']
    
    df['점수차'] = (df['재산정점수'] - df['original_score']).round(2)
    
    return df


def sensitivity_analysis(df):
    """4개 시나리오별 점수 계산 → 민감도 비교"""
    result = df[['no', 'name', 'region', 'original_score']].copy()
    
    for scenario, weights in WEIGHT_SCENARIOS.items():
        scored = compute_scores(df, weights=weights)
        result[f'점수_{scenario}'] = scored['재산정점수']
    
    # 시나리오별 편차 (변동성)
    score_cols = [f'점수_{s}' for s in WEIGHT_SCENARIOS.keys()]
    result['시나리오_표준편차'] = result[score_cols].std(axis=1).round(3)
    result['시나리오_범위'] = (
        result[score_cols].max(axis=1) - result[score_cols].min(axis=1)
    ).round(3)
    
    return result


