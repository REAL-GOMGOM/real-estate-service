def main():
    print("=" * 70)
    print("입지등급 점수 산정 모델 v1.0")
    print(f"기준일: 2026-04-18")
    print("=" * 70)
    
    df = build_dataframe()
    print(f"\n[1단계] 원시 데이터 로드: {len(df)}개 지역")
    print(f"       (평당가 유효: {df['평당가'].notna().sum()}개, "
          f"_NA: {df['평당가'].isna().sum()}개)")
    
    # Base 시나리오 점수 계산
    scored = compute_scores(df, weights=WEIGHT_SCENARIOS['Base'])
    print(f"\n[2단계] Base 시나리오 점수 산정 완료")
    print(f"       평균 점수차: {scored['점수차'].mean():.2f}")
    print(f"       가장 큰 상향 조정 Top 3:")
    for _, r in scored.nsmallest(3, '점수차').iterrows():
        print(f"         {r['no']:3d} {r['name']:12s} "
              f"{r['original_score']:.1f} → {r['재산정점수']:.2f} "
              f"({r['점수차']:+.2f})")
    print(f"       가장 큰 하향 조정 Top 3:")
    for _, r in scored.nlargest(3, '점수차').iterrows():
        print(f"         {r['no']:3d} {r['name']:12s} "
              f"{r['original_score']:.1f} → {r['재산정점수']:.2f} "
              f"({r['점수차']:+.2f})")
    
    # 민감도 분석
    sens = sensitivity_analysis(df)
    print(f"\n[3단계] 민감도 분석 (4개 시나리오)")
    print(f"       시나리오 간 점수 변동 표준편차 Top 5 (가장 민감한 지역):")
    for _, r in sens.nlargest(5, '시나리오_표준편차').iterrows():
        print(f"         {r['no']:3d} {r['name']:12s} "
              f"σ={r['시나리오_표준편차']:.3f} "
              f"(range={r['시나리오_범위']:.3f})")
    
    # 출력
    scored.to_csv('/mnt/user-data/outputs/scored_base.csv', 
                  index=False, encoding='utf-8-sig')
    sens.to_csv('/mnt/user-data/outputs/scored_sensitivity.csv',
                index=False, encoding='utf-8-sig')
    
    print(f"\n[4단계] 출력 저장 완료")
    print(f"       scored_base.csv ({len(scored)}행)")
    print(f"       scored_sensitivity.csv ({len(sens)}행)")
    
    return scored, sens


if __name__ == '__main__':
    scored, sens = main()
