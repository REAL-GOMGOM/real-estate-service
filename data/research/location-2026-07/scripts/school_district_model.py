def build_school_df():
    cols = ["순위", "지역", "서울대진학률", "4년제진학률", "특목자사고진학률", "학원수", "배정학군정성"]
    return pd.DataFrame(SCHOOL_DATA, columns=cols)

def compute_school_scores(df):
    """학군 점수(0~10) 산정"""
    df = df.copy()
    
    # 정규화 함수
    def norm(s):
        return (s - s.min()) / (s.max() - s.min() + 1e-9)
    
    df['z_서울대'] = norm(df['서울대진학률'])
    df['z_특목고'] = norm(df['특목자사고진학률'])
    df['z_학원수'] = norm(np.log1p(df['학원수']))
    
    df['학군점수'] = 10 * (
        0.35 * df['z_서울대'] +
        0.30 * df['z_특목고'] +
        0.20 * df['z_학원수'] +
        0.15 * df['배정학군정성']
    )
    df['학군점수'] = df['학군점수'].round(2)
    
    return df.sort_values('학군점수', ascending=False)

