def build_apartment_df():
    cols = ["단지명", "지역key", "자치구", "역세권_m", "실거래평당_만원",
            "세대수", "준공연차", "재건축성", "초품아", "브랜드", "신뢰도"]
    return pd.DataFrame(SEED_APARTMENTS, columns=cols)

def compute_apartment_scores(df):
    """단지별 입지점수 산정"""
    df = df.copy()
    
    # 지역점수 매핑
    df['지역점수'] = df['지역key'].map(REGION_SCORE)
    
    # 지역 대비 평당가 배수 (자치구 평균 대비)
    # 자치구 평균 평당가는 실거래평당 자체의 그룹평균으로 근사
    gu_mean = df.groupby('자치구')['실거래평당_만원'].transform('mean')
    df['평당배수'] = df['실거래평당_만원'] / gu_mean
    
    # z-score 정규화 함수
    def norm(s):
        return (s - s.min()) / (s.max() - s.min() + 1e-9)
    
    # 역세권: 가까울수록 높은 점수 (역수 방향)
    df['z_역세권'] = 1 - norm(df['역세권_m'])
    df['z_평당'] = norm(df['평당배수'])
    df['z_세대'] = norm(np.log1p(df['세대수']))
    # 재건축성/신축성: U자형 — 신축(연차<8)도 높고, 재건축임박(재건축성>=8)도 높음
    # 준공연차가 낮으면 신축프리미엄, 재건축성이 높으면 재건축프리미엄
    신축성 = df['준공연차'].apply(lambda x: max(0, 1 - x/15))  # 0년=1, 15년+=0
    재건축임박도 = (df['재건축성'] / 10).clip(0, 1)
    df['z_재건축'] = np.maximum(신축성, 재건축임박도)  # 둘 중 높은 값
    df['z_초품아'] = df['초품아']  # 0 or 1
    df['z_브랜드'] = norm(df['브랜드'])
    
    # 단지 프리미엄 가중합
    df['프리미엄_raw'] = (
        0.30 * df['z_역세권'] +
        0.20 * df['z_평당'] +
        0.15 * df['z_세대'] +
        0.15 * df['z_재건축'] +
        0.10 * df['z_초품아'] +
        0.10 * df['z_브랜드']
    )
    
    # sigmoid 변환 (0~1)
    # raw는 이미 0~1 근처이므로 중심 0.5로 조정 후 sigmoid
    df['프리미엄'] = 1 / (1 + np.exp(-6 * (df['프리미엄_raw'] - 0.5)))
    
    # 단지 자체 점수 (1~5)
    df['단지자체점수'] = 5.0 - 4.0 * df['프리미엄']
    
    # 최종: 지역점수 50% + 단지자체 50%
    df['단지입지점수'] = (df['지역점수'] * 0.5 + df['단지자체점수'] * 0.5).round(2)
    
    return df.sort_values('단지입지점수')

def get_district_champions(scored):
    """자치구별 대장 아파트(최상급=최저점) 추출"""
    champ = scored.loc[scored.groupby('자치구')['단지입지점수'].idxmin()].copy()
    champ = champ.sort_values('단지입지점수').reset_index(drop=True)
    return champ

